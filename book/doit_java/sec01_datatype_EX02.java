package sec01_datatype.EX02_NamingVariableAndConstant;

public class NamingVariableAndConstant {
	public static void main(String[] args) {
		// 변수
		boolean aBcD;
		byte 가나다; // 한글 네이밍 권장X
		short _abcd;
		char $ab_cd;
		long abcd3;
		double main;
		String myClassName;
		int ABC; // 변수 전부 대문자 권장X
		// 상수
		final double PI;
		final int MY_DATA;
		final float myData; // 변수명과 헷갈리니 권장X
	}
}
